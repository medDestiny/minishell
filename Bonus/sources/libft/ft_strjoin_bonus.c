#include "../../include/minishell_bonus.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	size_t	len;
	size_t	i;
	size_t	j;
	char	*join;

	i = 0;
	j = 0;
	if (!s1 && !s2)
		return (NULL);
	if (!s1)
		return (ft_strdup(s2));
	else if (!s2)
		return (ft_strdup(s1));
	len = ft_strlen(s1) + ft_strlen(s2);
	join = (char *)malloc(sizeof(char) * (len + 1));
	if (!join)
		return (NULL);
	while (s1[i] && i < ft_strlen(s1))
	{
		join[i] = s1[i];
		i++;
	}
	ft_strlcpy((join + i), s2, ft_strlen(s2) + 1);
	return (join);
}
